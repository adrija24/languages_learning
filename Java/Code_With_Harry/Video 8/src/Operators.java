public class Operators {

	public static void main(String[] args) {
		// Arithmetic operator
		int a = 4;
		int b = 10%a; 
		System.out.println(b); // Module operator
		int c = 9;
		c += 3;
		System.out.println(c);
		
		 // Comparison operator
		System.out.println(6==8);
		
		// Logical operator
		System.out.println(65>10 && 6==6);
		System.out.println(65<10 || 6==8);
		
		// Bitwise operator
		System.out.println(2&3);
	}
}